package Codeup_70_90;

import java.util.Scanner;

public class Code1077_For_정방향출력 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt();
		
		
		for (int i = 0; i <= n ; i++) {
			System.out.println(i);
		}
		sc.close();
	}

}
